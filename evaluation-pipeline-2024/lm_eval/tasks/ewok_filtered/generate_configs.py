import yaml

all_subtasks = [
    "agent-properties",
    "material-dynamics",
    "material-properties",
    "physical-dynamics",
    "physical-interactions",
    "physical-relations",
    "quantitative-properties",
    "social-interactions",
    "social-properties",
    "social-relations",
    "spatial-relations"
]

def main() -> None:
    for task in all_subtasks:
        file_name = f"{task}.yaml"
        try:
            with open(f"{file_name}", "w", encoding="utf-8") as f:
                f.write("# Generated by utils.py\n")
                yaml.dump(
                    {
                        "include": "_template_yaml",
                        "task": "ewok_" + task + "_filtered",
                        "dataset_kwargs": {"data_files": f"evaluation_data/ewok_filtered/{task}.jsonl"},
                    },
                    f,
                )
        except FileExistsError:
            pass


if __name__ == "__main__":
    main()
