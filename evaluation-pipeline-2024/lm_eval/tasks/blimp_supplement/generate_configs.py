import yaml


all_subtasks = [
    "hypernym",
    "qa_congruence_easy",
    "qa_congruence_tricky",
    "subject_aux_inversion",
    "turn_taking",
]


def main() -> None:
    for task in all_subtasks:
        file_name = f"{task}.yaml"
        try:
            with open(f"{file_name}", "w", encoding="utf-8") as f:
                f.write("# Generated by utils.py\n")
                yaml.dump(
                    {
                        "include": "_template_yaml",
                        "task": "blimp_supplement_" + task,
                        "dataset_kwargs": {"data_files": f"evaluation_data/supplement_filtered/{task}.jsonl"}
                    },
                    f,
                )
        except FileExistsError:
            pass


if __name__ == "__main__":
    main()
